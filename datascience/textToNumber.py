numberEntered = str(input('Enter a number (text) between 1 and 5\n>>>'))
if numberEntered == 'one':
    print('>>> 1')
elif numberEntered == 'two':
    print('>>> 2')
elif numberEntered == 'three':
    print('>>> 3')
elif numberEntered == 'four':
    print('>>> 4')
elif numberEntered == 'five':
    print('>>> 5')
else: print('You did not enter a valid value')
